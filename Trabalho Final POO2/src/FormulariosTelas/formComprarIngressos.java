    private void buttonComprarIngressoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonComprarIngressoActionPerformed
        int idUser = dadosList.indexUser(userLogado);
        JOptionPane.showMessageDialog(rootPane, "INGRESSO PARA O EVENTO " 
                + eventosListaMostrar.get(indexIngresso).getNomeEvento() 
                + " COMPRADO COM SUCESSO\nRESTAM " 
                + (eventosListaMostrar.get(indexIngresso).getQntdIngressos()-1) 
                + " INGRESSOS.");
        dadosList.compraIngresso(indexIngresso, idUser);
        formComprarIngressos telaComprarIngressos = new formComprarIngressos(dadosList.getEventos());
        telaComprarIngressos.carregarDados(dadosList);
        telaComprarIngressos.getUser(userLogado);
        this.setVisible(false);
        telaComprarIngressos.setVisible(true);
    }//GEN-LAST:event_buttonComprarIngressoActionPerformed

